package LISTA2;
import java.util.Scanner;
public class ex12 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Digite N: ");
        int N = sc.nextInt();
        for (int i = 0; i <= N; i++) {
            System.out.print(i + " ");
        }
    }
}
